#include <ctype.h>
#ifndef BIBLIOTECA_H_
#define BIBLIOTECA_H_


/** \brief Funcion para sumar los numeros ingresados.
 *
 * \param float Primer numero ingresado.
 * \param float Segundo numero ingresado.
 * \return float La suma de los dos numeros.
 *
 */
float sumar(float, float);


/** \brief Funcion para multiplicar los numero ingresados.
 *
 * \param float Primer numero ingresado.
 * \param float Segundo numero ingresado.
 * \return float La suma de los dos numeros.
 *
 */
float multiplicaciones (float, float);

/** \brief Funcion para restar los numero ingresados.
 *
 * \param float Primer numero Ingresado.
 * \param float Segundo numero ingresado.
 * \return float La suma de los dos numeros.
 *
 */
float restar (float, float);

/** \brief Funcion para dividir los numero ingresados.
 *
 * \param float Primer numero Ingresado.
 * \param float Segundo numero ingresado.
 * \return float La division de los dos numeros.
 *
 */
float divisiones (float, float);

/** \brief Funcion para sacar la factorial de los numeros ingresados.
 *
 * \param int Numeros ingresados.
 * \return int La factorial de los numeros ingresados.
 *
 */
int calcularFactorial(int);





#endif /* BIBLIOTECA_H_ */
